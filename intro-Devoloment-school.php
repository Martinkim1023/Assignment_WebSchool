						<p>Trường THPT Trường Thi (trước kia là THPT Trần Xuân Soạn) được thành lập theo Quyết định số 1690/QĐ-UBND của Chủ tịch UBND tỉnh Thanh Hóa ngày 8 - 8 - 1997. <br>

	Trong suốt quá trình 15 năm xây dựng và phát triển, từ những buổi đầu tiên thành lập trường đến nay, nhà trường luôn luôn nhận được sự quan tâm nhiệt tình của lãnh đạo các cấp tỉnh, Thành phố, của Sở Giáo dục và Đào tạo tỉnh Thanh Hóa; sự quan tâm, gắn bó của Hội phụ huynh học sinh, Hội khuyến học cũng như dân cư khối phố tạo điều kiện thuận lợi cho nhà trường hoạt động. <br>

	Thời gian đầu mới thành lập, ban lãnh đạo nhà trường cũng như thầy và trò vô cùng vất vả vì thiếu thốn cơ sở vật chất. Trường nằm ngay trong Cơ quan Mặt trận (nay là khu tượng đài tưởng niệm Hồ Chí Minh), phòng học là những nhà làm việc cũ được cải tạo, tu sửa lại để thầy và trò có chỗ giảng dạy, học tập. Đến năm 2000, theo yêu cầu của UBND Thành phố nhà trường phải di dời để thực hiện Dự án xây dựng khu tượng đài tưởng niệm Bác. Với tâm huyết và quyết tâm của ban lãnh đạo, nhà trường đã hoạt động trở lại trên nền khu xí nghiệp gỗ Điện Biên cũ tại địa chỉ ngõ 20 Ngô Tự - phường Lam Sơn - thành phố Thanh Hóa. Tuy nhiên, vì diện tích eo hẹp chỉ 2000m2 đất, không đủ phòng học phục vụ nhu cầu học tập của học sinh, một số lớp phải học nhờ trong khuôn viên của trường THPT Hàm Rồng để chờ xây dựng cơ sở mới. <br>

	Trong khoảng thời gian 6 năm hoạt động tại cơ sở này (từ năm 2001 đến năm 2007), vì điều kiện trường nằm quá sâu trong hẻm, đường đi lối lại nhỏ hẹp, khu vực dân cư phức tạp nên công tác quản lý, giảng dạy và học tập gặp rất nhiều khó khăn. Tuy vậy, Ban lãnh đạo nhà trường cũng như thầy trò vẫn động viên, quyết tâm thi đua dạy và học thật tốt nên đã hoàn thành xuất sắc các nhiệm vụ được giao cũng như công tác tuyển sinh hàng năm. Tỷ lệ thi đỗ tốt nghiệp đạt từ 80% - 85% với 4852 học sinh trong 9 khóa học. Trong đó số lượng đỗ tốt nghiệp loại khá, giỏi là 323 em; đạt giải thi học sinh giỏi cấp Tỉnh có 234 em, nhiều sinh viên các trường Đại học, Cao đẳng, trung cấp chính là những học sinh đã trưởng thành từ mái trường thân yêu này. <br>

	Trước tình hình khó khăn, thiếu thốn về cơ sở vật chất, năm 2007, Hội đồng quản trị, Ban giám hiệu nhà trường đã lập dự án trình lên các cấp lãnh đạo xin chuyển địa điểm. Sau một thời gian dày công tìm kiếm, nghiên cứu, giải trình, trường đã được Chủ tịch UBND tỉnh, thành phố chấp thuận cho chuyển sang vị trí mới theo qui hoạch mạng lưới trường học và nhận Quyết định giao đất của UBND tỉnh tại số 81 đường Trường Thi - phường Trường Thi - thành phố Thanh Hoá với hơn 5000m2 đất, giao thông thuận tiện. Ngày 2 tháng 8 năm 2007, được sự đồng ý của lãnh đạo các cấp, Sở giáo dục đào tạo và sự ủng hộ của nhân dân phường Trường Thi, nhà trường đã tổ chức lễ động thổ khởi công đầu tư xây dựng trường. Sau 12 tháng với sự quyết tâm cao của Hội đồng quản trị, Ban giám hiệu nhà trường cùng với đơn vị thi công, công trình đã hoàn thành và đưa vào sử dụng với: <br>
	 
	-   1 khu nhà lớp học 3 tầng, bao gồm: <br>
	-   19 phòng học tiêu chuẩn cấp Quốc gia <br>
	-   03 phòng học chức năng tiêu chuẩn: phòng máy tính, phòng thí nghiệm, phòng máy chiếu <br>
	-   03 phòng chờ giáo viên <br>
	-   Thư viện <br>
	-   1 khu nhà hiệu bộ 2 tầng, bao gồm: <br>
	  1 Hội trường lớn với sức chứa 150 người
	  7 phòng làm việc cho Ban lãnh đạo nhà trường
	Tại địa điểm mới, với cơ sở vật chất khang trang, to đẹp, lãnh đạo nhà trường tiếp tục đầu tư trang thiết bị giảng dạy và học tập hiện đại, tân tiến tạo điều kiện tốt nhất cho hoạt động dạy và học lâu dài. Vì đây là mô hình trường ngoài công lập nên gặp rất nhiều khó khăn trong công tác xây dựng thương hiệu, uy tín và do vậy cũng ảnh hưởng không nhỏ đến công tác tuyển sinh, đời sống của cán bộ, giáo viên. Điều này chỉ có thể chứng minh bằng những kết quả, thành tích đạt được trong quá trình phát triển của nhà trường. Nhận thức được vấn đề đó, Hội đồng quản trị và Ban giám hiệu rất chú trọng trong công tác quản lý của nhà trường, chỉ đạo và điều hành các hoạt động giảng dạy và học tập hết sức chặt chẽ và khoa học. Nhà trường thường xuyên tổ chức các buổi tọa đàm, thảo luận nhằm mở rộng kiến thức cho giáo viên; liên tục phân công giáo viên tham gia các chương trình tập huấn, bồi dưỡng nâng cao trình độ; xây dựng quy chế khen thưởng phù hợp để khuyến khích, động viên giáo viên dạy tốt, học sinh đạt thành tích cao… Bên cạnh đó, nhà trường cũng quan tâm công tác xây dựng, gìn giữ kỉ cương, nề nếp học sinh, nề nếp từng tiết dạy, tiết học nhằm nâng cao chất lượng giờ học. Các tổ chức đoàn thể trong nhà trường cũng có những hoạt động thiết thực hỗ trợ đắc lực cho hoạt động chuyên môn, đặc biệt là Đoàn thanh niên Cộng sản Hồ Chí Minh, đã tổ chức nhiều phong trào có ý nghĩa giáo dục sâu sắc như: “Tuổi trẻ học tập, rèn luyện vì ngày mai lập thân lập nghiệp”, “Nói không với tiêu cực trong thi cử và bệnh thành tích trong giáo dục”… <br>

	Đồng hành cùng với nhà trường còn có sự quan tâm, đóng góp ý kiến xây dựng trường của Ban đại diện cha mẹ học sinh. Ban đã có nhiều hoạt động hỗ trợ nhà trường bằng nhiều hình thức ý nghĩa như: trồng cây xanh, tặng ghế đá sân trường, tặng quà các dịp lễ tết của thầy cô và đặc biệt là dành nhiều phần thưởng cho các cá nhân, tập thể lớp đạt thành tích cao trong học tập, trong các kỳ thi học sinh giỏi ... <br>

	Trong năm học mới này, với chủ đề trọng tâm: "Đổi mới căn bản, toàn diện giáo dục và đào tạo" nhằm đáp ứng yêu cầu ngày càng cao của ngành giáo dục, thầy trò Trường THPT Trường Thi quyết tâm vượt qua thử thách, hoàn thành tốt những nhiệm vụ chính sau:<br>
	Kiên định với mục tiêu giáo dục toàn diện. Mỗi học sinh cần có nền tảng kiến thức cơ bản, vững chắc về các lĩnh vực khoa học xã hội, khoa học tự nhiên; tích cực tham gia các hoạt động giáo dục ngoài giờ, các hoạt động xã hội; rèn luyện kỹ năng sống; có thái độ đúng đắn trước các vấn đề của cuộc sống; chống lại các hiện tượng tiêu cực; chống các biểu hiện ăn chơi, đua đòi, hưởng thụ.  <br>
	Tiếp tục chú trọng các mục tiêu về chuyên môn, nâng cao hơn nữa thành tích học sinh giỏi trên nền giáo dục toàn diện; tập trung giáo dục mũi nhọn, phấn đấu có nhiều học sinh đạt giải cao trong các kỳ thi học sinh giỏi; tiếp tục giữ vững và nâng cao tỉ lệ học sinh đỗ tốt nghiệp và đại học, có nhiều học sinh đỗ điểm cao. <br>
	Mỗi thầy giáo, cô giáo cần tiếp tục tự học, tự bồi dưỡng nâng cao trình độ chuyên môn, nghiệp vụ, hiểu biết pháp luật. Khai thác và sử dụng có hiệu quả các thiết bị dạy học; tích cực cải tiến nội dung, phương pháp giảng dạy. <br>
	Tiếp tục đẩy mạnh ứng dụng công nghệ thông tin trong các hoạt động dạy và học; nâng cao hiệu quả tự học, tự bồi dưỡng cho cả thầy và trò. 
	Triển khai phong trào thi đua “Xây dựng trường học thân thiện, học sinh tích cực” nhằm tạo một bước đột phá trong giáo dục toàn diện, rèn luyện kỹ năng sống, tổ chức hoạt động tập thể vui tươi, lành mạnh cho học sinh; tiếp tục nâng cấp và xây dựng cơ sở vật chất tiến tới xây dựng trường đạt chuẩn quốc gia. <br>

	Trải qua 19 năm với bao thăng trầm, gian nan vất vả, trường THPT Trường Thi luôn tự hào với đội ngũ giáo viên có trình độ chuyên môn vững vàng, tâm huyết với nghề nghiệp, với thế hệ trẻ, luôn có ý thức cố gắng, phấn đấu hết mình cho sự nghiệp giáo dục cao cả. Đến nay chúng ta có thể khẳng định ngôi trường này chính thực là nơi đất học, đất thi của phường Trường Thi, lâu dài và mãi mãi. Nhà trường mỗi ngày một phát triển, từng bước khẳng định uy tín, chất lượng giáo dục, là niềm tự hào và tin yêu của không những thầy và trò trường THPT Trường Thi mà còn của cả các bậc phụ huynh, chính quyền và nhân dân địa phương. <br>
